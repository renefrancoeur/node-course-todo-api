var mongoose = require('mongoose');

// todo model
//model, takes 2 arguments, (name, object)
var Todo = mongoose.model('Todo', {
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  }
});

module.exports = {Todo};// same as Todo = Todo
